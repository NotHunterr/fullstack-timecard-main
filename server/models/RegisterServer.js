const mongoose = require('mongoose')
const schema = mongoose.Schema

const RegisterSchema = new mongoose.Schema({
  firstName: {
    type: String,
    required: true,
  },
  lastName: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  username: {
    type: String,
    required: true,
  },
  password: {
    type: String,
    required: true,
  },
})

const RegisterModel = mongoose.model('registerdatabase', RegisterSchema)
module.exports = RegisterModel
